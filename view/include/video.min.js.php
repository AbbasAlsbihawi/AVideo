<?php
global $global, $advancedCustom;
$disableYoutubeIntegration = @$advancedCustom->disableYoutubePlayerIntegration || isMobile();
$objPlayerSkins = AVideoPlugin::getObjectData('PlayerSkins');
echo "<!-- including video-js -->" . PHP_EOL;
if($objPlayerSkins->useShakaPlayer){
    echo '<script src="' . getURL('node_modules/shaka-player/dist/shaka-player.compiled.debug.js') . '" type="text/javascript"></script>';
}
if (!empty($global['forceOldChrome']) || isOldChromeVersion()) {
    echo '<script src="' . getURL('view/js/video-js-olderversion/video.min.js') . '" type="text/javascript"></script>';
} else {
    echo '<script src="' . getURL('node_modules/video.js/dist/video.min.js') . '" type="text/javascript"></script>';
}
