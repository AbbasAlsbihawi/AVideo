}
if ((isset($_GET['isEmbedded'])) && empty($disableYoutubeIntegration)) {
    if ($_GET['isEmbedded'] == "y") {
        ?>
        <script src="<?php echo getURL('view/js/videojs-youtube/Youtube.js'); ?>" type="text/javascript"></script>
        <?php
    } elseif ($_GET['isEmbedded'] == "v") {
        ?>
        <script src="<?php echo getCDN('view/js/videojs-vimeo/videojs-vimeo.js'); ?>" type="text/javascript"></script>
        <?php
    }
}